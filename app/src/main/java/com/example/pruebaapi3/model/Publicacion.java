package com.example.pruebaapi3.model;

public class Publicacion {

    private String nombre;
    private String segundonombre;
    private String segundonombrepadre;
    private String nombrecompleto;
    private String age;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getSegundonombre() {
        return segundonombre;
    }

    public void setSegundonombre(String segundonombre) {
        this.segundonombre = segundonombre;
    }

    public String getSegundonombrepadre() {
        return segundonombrepadre;
    }

    public void setSegundonombrepadre(String segundonombrepadre) {
        this.segundonombrepadre = segundonombrepadre;
    }

    public String getNombrecompleto() {
        return nombrecompleto;
    }

    public void setNombrecompleto(String nombrecompleto) {
        this.nombrecompleto = nombrecompleto;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
